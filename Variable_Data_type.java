
package basic.java;

public class Variable_Data_type 

{
    public static void main(String[] args ) //SHORTCUT (psvm+Tab)
    {
        boolean bo=true; //Dynamic Initialization
        char x='A';
        int a = 10;
        float f = (float) 5.5; 
        double d = 5.5;
         
    System.out.printf("\nBoolean = %b" , bo);       //SHORTCUT(sout)
    System.out.printf("\nCharacter =  %c" , x);
    System.out.printf("\nInteger = %d" ,  a);
    System.out.printf("\nFloat = %.2f" , f);
    System.out.printf("\nDouble = %f" , d);
     
   
    
    }
}
