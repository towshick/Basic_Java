
package basic.java;

public class Variable_Data_type2 

{
    public static void main(String[] args )
    {
        boolean bo=true; //Dynamic Initialization
        char x='A';
        int a = 10;
        float f = (float) 5.5; 
        double d = 5.5;
         
    System.out.println("Boolean = %b"  +bo);
    System.out.println("Character =  %c"  +x);
    System.out.println("Integer = %d"   +a);
    System.out.println("Float = "  +f);
    System.out.println("Double = "  +d);
    
    
    }
}
